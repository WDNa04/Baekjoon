number = int(input("Type the radius of the sphere: "))
π = 3.141592
volume = 4/3*π*(number**3)
area = 4*π*(number**2)
print("The volume of the sphere is", str(volume)+".")
print("The volume of the sphere is {}.".format(volume))
print(f"The volume of the sphere is {volume}.")
print("The area of the sphere is", str(area)+".")
print("The area of the sphere is {}.".format(area))
print(f"The area of the sphere is {area}.")
